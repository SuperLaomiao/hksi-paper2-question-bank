function buildStem(chapter, index, format) {
  const number = String(index + 1).padStart(2, "0");
  if (format === "數字期限") {
    return `第二階段 ${chapter}-${number}｜以下關於數字、時限或門檻的陳述，哪一項最準確？`;
  }
  if (format === "案例") {
    return `第二階段 ${chapter}-${number}｜一名從業員正在處理本章相關事項，下列哪一項判斷最恰當？`;
  }
  if (format === "比較") {
    return `第二階段 ${chapter}-${number}｜比較本章各項規則，下列哪一項陳述最準確？`;
  }
  return `第二階段 ${chapter}-${number}｜就本章監管細節而言，下列哪一項陳述最準確？`;
}

function insertAt(items, value, index) {
  const result = [...items];
  result.splice(index, 0, value);
  return result;
}

export const phaseTwoQuestions = Object.entries(factsByChapter).flatMap(([chapterKey, facts]) => {
  const chapter = Number(chapterKey);
  if (facts.length !== 30) throw new Error(`Phase two chapter ${chapter} must contain 30 facts.`);

  return facts.map(([topic, correct, misconception], index) => {
    const format = formatLabels[index % formatLabels.length];
    const answer = (index * 3 + chapter) % 4;
    const distractors = [
      misconception,
      facts[(index + 9) % facts.length][2],
      facts[(index + 19) % facts.length][2]
    ];

    return {
      id: `P2-C${chapter}-S2-${String(index + 1).padStart(3, "0")}`,
      chapter,
      phase: 2,
      format,
      topic,
      difficulty: format === "案例" || format === "比較" ? "進階" : "高頻",
      stem: buildStem(chapter, index, format),
      options: insertAt(distractors, correct, answer),
      answer,
      explanation: `${correct} 其餘選項混淆了本章不同規則的適用範圍、責任或期限。`,
      source: `HKSI Paper 2 v2.8.1 第${chapter}章（第二階段：${chapterTopics[chapter]}）`
    };
  });
});
